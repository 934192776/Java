package com.itheima.operator;

public class OperatorDemo6 {
    public static void main(String[] args) {
        // 实现字母的大小写转换，将大写字母转换为小写字母
        // A -----> a

        // 定义变量记录大写的字符
        char c = 'A'; 
        // 2. 转成小写
        // 65 + 32 = 97
        char cc = (char)(c + 32);
        System.out.println(cc);

    
    
    }

}
