package com.itheima.operator;

public class OperatorDemo7 {
    public static void main(String[] args) {
        /* 
         = 直接赋值
         += 后厚赋值
         -= 减后赋值
         *= 乘后赋值
         /= 除后赋值
         %= 取模后赋值       
        */

         int a = 10;
         int b = 20;
         // a + b ----->a
         a += b;

         System.out.println(a);
         System.out.println(b);
    
    }
}
